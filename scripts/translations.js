let currentUILanguage = 'ku-BHD';

// فەنکشنا گوهۆڕینا زمانی وێبسایتێ
function changeUILanguage(lang) {
    currentUILanguage = lang;
    localStorage.setItem('uiLanguage', lang);
    
    // نویکرنا هەمی تێکستان
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (TRANSLATIONS[lang] && TRANSLATIONS[lang][key]) {
            element.textContent = TRANSLATIONS[lang][key];
        }
    });
    
    // نویکرنا ئاراستەیا تێکستی (RTL/LTR)
    const rtlLanguages = ['ku-BHD', 'ku-SOR', 'ar', 'fa'];
    if (rtlLanguages.includes(lang)) {
        document.documentElement.dir = 'rtl';
        document.documentElement.lang = lang;
    } else {
        document.documentElement.dir = 'ltr';
        document.documentElement.lang = lang;
    }
    
    // نویکرنا پلەیهۆڵدەران
    document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
        const key = element.getAttribute('data-translate-placeholder');
        if (TRANSLATIONS[lang] && TRANSLATIONS[lang][key]) {
            element.placeholder = TRANSLATIONS[lang][key];
        }
    });
    
    console.log('🌍 زمان گوهۆری بو: ' + lang);
}

// بارکرنا زمانی هاتینە پاراستن
window.addEventListener('load', function() {
    const savedLang = localStorage.getItem('uiLanguage') || 'ku-BHD';
    const langSelect = document.getElementById('uiLanguage');
    if (langSelect) {
        langSelect.value = savedLang;
        changeUILanguage(savedLang);
    }
});
